    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        String ID = edtID.getText();
        String Password = String.valueOf(edtPassword.getPassword());
        String Confirm = String.valueOf(edtConfirm.getPassword());
        boolean valid = true;
                
        if(ID.equals("") || ID.length() != 13)
        {
            JOptionPane.showMessageDialog(null, "Add your ID or check it");
            valid = false;
            pbarSignUp.setValue(pbarSignUp.getValue() + 20);
            pbarSignUp.setString(Integer.toString(pbarSignUp.getValue()) + "% Invalid");
        }
        
        if(Password.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Add A Password");
            valid = false;
            pbarSignUp.setValue(pbarSignUp.getValue() + 20);
            pbarSignUp.setString(Integer.toString(pbarSignUp.getValue()) + "% Invalid");
        }
        
        
        if(!Password.equals(Confirm))
        {
            JOptionPane.showMessageDialog(null, "Retype The Password Again");
            valid = false;
            pbarSignUp.setValue(pbarSignUp.getValue() + 20);
            pbarSignUp.setString(Integer.toString(pbarSignUp.getValue()) + "% Invalid");
        }
            
        if(checkUsername(ID))
        {
            JOptionPane.showMessageDialog(null, "This ID Already Exist");
            valid = false;
            pbarSignUp.setValue(pbarSignUp.getValue() + 20);
            pbarSignUp.setString(Integer.toString(pbarSignUp.getValue()) + "% Invalid");
        }
            
        for (int i = 0; i < ID.length(); i++) 
        {
            if (!Character.isDigit(ID.charAt(i))) {
                valid = false;
                pbarSignUp.setValue(pbarSignUp.getValue() + 20);
                pbarSignUp.setString(Integer.toString(pbarSignUp.getValue()) + "% Invalid");
            }
        }
        
        if (valid)
        {
            try {
                CinemaSprintJava.DBConnection();
                PreparedStatement ps;
                String query = "INSERT INTO tbl_contacts(IDnum, Password) VALUES (?,?)";
                ps = CinemaSprintJava.conn.prepareStatement(query);

                ps.setString(1, ID);
                ps.setString(2, Password);

                if(ps.executeUpdate() > 0)
                {
                    JOptionPane.showMessageDialog(null, "New User Add");
                    SignIn frmSignIn = new SignIn();
                    frmSignIn.setVisible(true);
                    this.dispose();
                }

                CinemaSprintJava.conn.close();
            } catch (SQLException ex) {
                Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
             JOptionPane.showMessageDialog(null, "Failed to Add User");
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

